fun Type.compact(): String = when(this) {
    Type.COLORLESS -> "C"
    Type.DARKNESS -> "D"
    Type.DRAGON -> "N"
    Type.FAIRY -> "Y"
    Type.FIGHTING -> "F"
    Type.FIRE -> "R"
    Type.GRASS -> "G"
    Type.LIGHTNING -> "L"
    Type.METAL -> "M"
    Type.PSYCHIC -> "P"
    Type.WATER -> "W"
    Type.UNKNOWN -> ""
}


fun String.type(): Type = when(this.toUpperCase()) {
    "C" -> Type.COLORLESS
    "D" -> Type.DARKNESS
    "N" -> Type.DRAGON
    "Y" -> Type.FAIRY
    "F" -> Type.FIGHTING
    "R" -> Type.FIRE
    "G" -> Type.GRASS
    "L" -> Type.LIGHTNING
    "M" -> Type.METAL
    "P" -> Type.PSYCHIC
    "W" -> Type.WATER
    else -> Type.UNKNOWN
}

